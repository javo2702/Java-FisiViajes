    public void agregarFurgon(){
        Furgon fur = new Furgon();
        fur.setPlaca(placa.getText());
        fur.setMarca(marca.getText());
        fur.setCapacidadMaxima(Integer.parseInt(capacidad.getText()));
        if(rbSi.isSelected()==true)
            fur.setMantenimiento(true);
        else
            fur.setMantenimiento(false);
        emp.agregarVehiculo(fur);
        limpiarDatos();
        tablaFurgon.setModel(emp.mostrarFurgon());
    }
    
    public void modificarFurgon(){
        Furgon fur = new Furgon();
        fur.setPlaca(placa.getText());
        fur.setMarca(marca.getText());
        fur.setCapacidadMaxima(Integer.parseInt(capacidad.getText()));
        if(rbSi.isSelected()==true)
            fur.setMantenimiento(true);
        else
            fur.setMantenimiento(false);
        emp.actualizarFurgon(fur, tablaFurgon);
        tablaFurgon.setModel(emp.mostrarFurgon());
    }
    
    public void eliminarFurgon(){
        emp.eliminarFurgon(tablaFurgon);
        limpiarDatos();
        tablaFurgon.setModel(emp.mostrarFurgon());
    }
    
    public void mostrarTabla(int filaSeleccionada){
        placa.setText(tablaFurgon.getValueAt(filaSeleccionada, 1).toString());
        marca.setText(tablaFurgon.getValueAt(filaSeleccionada, 2).toString());
        if(tablaFurgon.getValueAt(filaSeleccionada, 3).toString().equals(1))
            rbSi.setSelected(true);
        else
            rbNo.setSelected(true);
        capacidad.setText(tablaFurgon.getValueAt(filaSeleccionada, 4).toString());
    }
    
    public void limpiarDatos(){
        placa.setText("");
        marca.setText("");
        capacidad.setText("");
        grupoFurgon.clearSelection();
        
    }

    public void llenarCombox(){
        ArrayList<String> datos = new ArrayList<>();
        datos = emp.conductores('f');
        for(int i=0;i<datos.size();i++){
            comboConductor.addItem(datos.get(i));
        }
        comboConductor.setSelectedItem(null);
        datos = emp.placas('f');
        for(int j=0;j<datos.size();j++){
            comboPlaca.addItem(datos.get(j));
        }
        comboPlaca.setSelectedItem(null);
    }
    
    public void agregarItinerario(){
        ItinerarioFurgon iF = new ItinerarioFurgon();
        iF.setRuta(ruta.getText());
        int indiceCond = comboConductor.getSelectedIndex();
        iF.setConductorDNI(comboConductor.getItemAt(indiceCond));
        int indicePlaca = comboPlaca.getSelectedIndex();
        iF.setPlacaFurgon(comboPlaca.getItemAt(indicePlaca));
        iF.setPrecioPorKilo(excepcionCosto());
        emp.agregarItinerario_Furgon(iF);
        limpiarCampos();
        tablaItinerarioFurgon.setModel(emp.mostrarDatosItFurgon());
    }
    
    public void modificarItinerario(){
        ItinerarioFurgon iF = new ItinerarioFurgon();
        iF.setRuta(ruta.getText());
        int indiceCond = comboConductor.getSelectedIndex();
        iF.setConductorDNI(comboConductor.getItemAt(indiceCond));
        int indicePlaca = comboPlaca.getSelectedIndex();
        iF.setPlacaFurgon(comboPlaca.getItemAt(indicePlaca));
        iF.setPrecioPorKilo(Double.parseDouble(costo.getText()));
        emp.actualizarItinerarioFurgon(iF, tablaItinerarioFurgon);
        limpiarCampos();
        tablaItinerarioFurgon.setModel(emp.mostrarDatosItFurgon());
    }
    
    public void mostrarTabla(int filaSeleccionada){
        ruta.setText(tablaItinerarioFurgon.getValueAt(filaSeleccionada, 1).toString());
        comboConductor.setSelectedItem(tablaItinerarioFurgon.getValueAt(filaSeleccionada, 2).toString());
        comboPlaca.setSelectedItem(tablaItinerarioFurgon.getValueAt(filaSeleccionada, 3).toString());
        costo.setText(tablaItinerarioFurgon.getValueAt(filaSeleccionada, 4).toString());
    }
    
    public void eliminarItinerario(){
        emp.eliminarDatosItinerarioFurgon(tablaItinerarioFurgon);
        limpiarCampos();
        tablaItinerarioFurgon.setModel(emp.mostrarDatosItFurgon());
    }
    
    public void limpiarCampos(){
        ruta.setText("");
        comboConductor.setSelectedItem(null);
        comboPlaca.setSelectedItem(null);
        costo.setText("");
    }

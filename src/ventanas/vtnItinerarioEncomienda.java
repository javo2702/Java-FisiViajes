    public double excepcionCosto(){
        boolean flag;
        double monto=0;
        String cadena = costo.getText();
        do{
            try{
                monto = Double.parseDouble(cadena);
                flag = false;
                costo.setBackground(Color.white);
            } catch(NumberFormatException e){
                costo.setBackground(Color.red);
                cadena = JOptionPane.showInputDialog("Ingrese un monto numerico");
                costo.setText(cadena);
                flag = true;
            }
        } while(flag);
        return monto;
    }
    

    public void agregarBus(){
        Bus b = new Bus();
        b.setPlaca(txtBusPlaca.getText());
        b.setMarca(txtBusMarca.getText());
        if(rbSiBus.isSelected()==true)
            b.setMantenimiento(true);
        else
            b.setMantenimiento(false);
        emp.agregarVehiculo(b);
        limpiarCampos();
        tablaBus.setModel(emp.mostrarBus());
    }
    
    public void modificarBus(){
        Bus bn = new Bus();
        bn.setPlaca(txtBusPlaca.getText());
        bn.setMarca(txtBusMarca.getText());
        if(rbSiBus.isSelected()==true)
            bn.setMantenimiento(true);
        else
            bn.setMantenimiento(false);
        emp.actualizarBus(bn, tablaBus);
        limpiarCampos();
        tablaBus.setModel(emp.mostrarBus());
    }
    
    public void eliminarBus(){
        emp.eliminarBus(tablaBus);
        limpiarCampos();
        tablaBus.setModel(emp.mostrarBus());
    }
    
    public void mostrarTabla(int filaSeleccionada){
        txtBusPlaca.setText(tablaBus.getValueAt(filaSeleccionada, 1).toString());
        txtBusMarca.setText(tablaBus.getValueAt(filaSeleccionada, 2).toString());
        if(tablaBus.getValueAt(filaSeleccionada, 3).toString().equals("1"))
            rbSiBus.setSelected(true);
        else
            rbNoBus.setSelected(true);
    }
    
    public void limpiarCampos(){
        txtBusPlaca.setText("");
        txtBusMarca.setText("");
        grupoBus.clearSelection();
    }
    

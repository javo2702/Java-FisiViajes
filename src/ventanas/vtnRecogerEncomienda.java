    public void realizarEntrega(){
        agencia.registrarEntregaEncomienda(codigo.getText());
        JOptionPane.showMessageDialog(null, "Entrega Realizada");
        limpiarCampos();
    }
    public void realizarBusqueda(){
        ArrayList<String> datos = agencia.buscarEncominedaDni(dni.getText());
        if(!datos.isEmpty()){
            codigo.setText(datos.get(0));
            cantidad.setText(datos.get(1));
            DefaultTableModel modelo = agencia.mostrarDetalles(codigo.getText());
            tablaEn.setModel(modelo);
        }
        else
            JOptionPane.showMessageDialog(null, "No tiene paquetes");
    }
    public void limpiarCampos(){
        dni.setText("");
        codigo.setText("");
        cantidad.setText("");
        tablaEn.setModel(null);
    }
    
